<template>
  <section class="app-main">
    <transition name="fade-transform" mode="out-in">
      <keep-alive :include="cachedViews">
        <router-view :key="key" />
      </keep-alive>
    </transition>
  </section>
</template>

<script>
export default {
  name: 'AppMain',
  computed: {
    key() {
      return this.$route.path
    },
    cachedViews() {
      const ary = []
      console.log(this.$store.state)
      const tag = this.$store.state.tagsView.cachedViews
      for (const t of tag) {
        const str = t
          .split('/')[t.split('/').length - 1].replace(/( |^)[a-z]/g, L => L.toUpperCase())
        ary.push(str)
      }
      return ary
    }
  }
}
</script>

